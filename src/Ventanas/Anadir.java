    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String usuario = this.nombre.getText().trim();

            if (!usuario.startsWith("@")) {
                usuario = "@" + usuario;
            }

            this.grafo.insertarVertice(usuario);
            this.actualizar();

            JOptionPane.showMessageDialog(rootPane, "Usuario agregado con éxito: " + usuario);
            this.seguido1.setSelectedItem(usuario);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al agregar usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

