    public void actualizar() {
        this.seguido.removeAllItems();
        this.seguido1.removeAllItems();

        Vertice aux = grafo.primero;
        while (aux != null) {
            // 3. Agregar el nombre de usuario de cada vértice al modelo.
            this.seguido.addItem(aux.username);
            this.seguido1.addItem(aux.username);
            // Moverse al siguiente vértice en la lista enlazada principal.
            aux = aux.sig;
        }
    }

